package utilidade;

public class Clsc {

    //quebra a linha 15 vezes
    public static void clsc(){

        for (int i = 1; i <= 15; i++){
            System.out.println();
        }

    }

    //quebra a linha n vezes
    public static void clsc(int n){
        for (int i = 1; i <= n; i++){
            System.out.println();
        }
    }


}
